export function normalizeCategories(categories = []) {
  const normalized = [];
  const seen = new Set();

  for (const category of categories) {
    const canonical = categoryNormalization.get(category) ?? category;

    if (!canonical || seen.has(canonical)) {
      continue;
    }

    seen.add(canonical);
    normalized.push(canonical);
  }

  return normalized;
}

